#include <stdio.h>

void    equation(int n)
{
	int i;
	int j;

	i = -1;
	while (++i < 100)
	{
		j = -1;
		while (++j < 100)
			if (i + j == n && i / 10 == j % 10)
				printf("A = %i, B = %i, C = %i\n", i / 10, i % 10, j / 10);
	}
}
